char	*ft_strcat(char *dest, const char *src)
{
	int 	i;
	int		y;

	i = 0;
	y = 0;
	while (dest[i])
		i++;
	while (src[y])
		dest[i++] = src[y++];
	dest[i] = '\0';
	return (dest);
}
