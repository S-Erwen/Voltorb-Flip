#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	int		i;
	char	*s1;

	s1 = (char *)s;
	i = 0;
	while (s1[i] && n > 0)
	{
		s1[i] = '\0';
		i++;
		n--;
	}
	s = s1;
}