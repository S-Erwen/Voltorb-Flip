#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	char			*s1;
	unsigned int	i;

	s1 = (char *)s;
	i = 0;
	while (n > i)
	{
		s1[i] = c;
		i++;
	}
	s = s1;
	return (s);
}